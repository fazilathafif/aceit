export function getQuestionsForConfig(
  subject: string,
  chapters: string[],
  difficulty: string,
  count: number
): Question[] {
  let pool = QUESTION_BANK.filter((q) => q.subject === subject);

  // Only apply chapter filter if it yields results; otherwise use full subject pool.
  if (chapters.length > 0) {
    const byChapter = pool.filter((q) => chapters.includes(q.chapter));
    if (byChapter.length > 0) pool = byChapter;
  }

  // Only apply difficulty filter if it yields results; otherwise keep broader pool.
  if (difficulty !== 'Mixed') {
    const byDifficulty = pool.filter((q) => q.difficulty === difficulty);
    if (byDifficulty.length > 0) pool = byDifficulty;
  }

  const shuffled = [...pool].sort(() => Math.random() - 0.5);
  return shuffled.slice(0, Math.min(count, shuffled.length));
}
